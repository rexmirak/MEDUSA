    ttp_ids = [ttp['id'] for ttp in ttp_list]
    
    # Combine matched and unmatched TTPs
    combined_ttps = set(matching_ttps).union(set(unmatching_ttps))
    
    # Check completeness
    is_complete = set(ttp_ids) == combined_ttps
    
    # Check for overlap
    is_disjoint = not set(matching_ttps).intersection(set(unmatching_ttps))
    
    # Total count
    total_count_matches = len(matching_ttps) + len(unmatching_ttps)
    expected_count = len(ttp_ids)
    
    # All checks
    return {
        "is_complete": is_complete,
        "is_disjoint": is_disjoint,
        "total_count_matches": total_count_matches == expected_count,
        "details": {
            "matching_count": len(matching_ttps),
            "unmatching_count": len(unmatching_ttps),
            "input_count": len(ttp_ids),
        }
    }

